package com.itfactory;

public class whileex3 {
        /**
         * Se da un numar x = 1.
         * Se se incrementeze numarul x pana cand acesta depaseste 5.
         */
        public static void main(String[] args)
        {
            int x = 1;
            while (x<=5) {
                System.out.println("Numarul este: " + x);
                x++;
            }
        }

}
